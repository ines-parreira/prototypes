export interface Props {
    csvColumns: CsvColumnPreview[]
    helpCenter: HelpCenter
    locales: Locale[]
    onImport: (mappings: GorgiasFieldsMappingsLocalized) => void
    onCancel: () => void
}

export const CsvColumnMatching = ({
    csvColumns,
    helpCenter,
    locales,
    onImport,
    onCancel,
}: Props): JSX.Element | null => {
    const csvColumnsByName: CsvColumnsByName = React.useMemo(() => {
        const columnsByName: CsvColumnsByName = new Map()
        csvColumns.forEach((column) => {
            columnsByName.set(column.name, column)
        })

        return columnsByName
    }, [csvColumns])

    const [fieldsMappings, setFieldsMappings] =
        useState<GorgiasFieldsMappingsLocalized>(
            initialMappings(
                helpCenter.default_locale,
                Array.from(csvColumnsByName.keys())
            )
        )

    const selectedLocalesToImport: LocaleCode[] = React.useMemo(
        () => fieldsMappings.map(({localeCode}) => localeCode),
        [fieldsMappings]
    )

    const handleSelectedLocalesChange = (newSelectedLocales: LocaleCode[]) => {
        setFieldsMappings(
            newSelectedLocales.sort().map((newSelectedLocale) => {
                const existingMapping = fieldsMappings.find(
                    ({localeCode}) => localeCode === newSelectedLocale
                )

                return (
                    existingMapping || {
                        localeCode: newSelectedLocale,
                        mappings: {},
                    }
                )
            })
        )
    }

    const makeOnChangeMapping =
        (field: GorgiasFields, localeCode: LocaleCode) =>
        (mapping?: ColumnSourceCsvOrAutoGenerated) => {
            if (mapping === undefined) {
                return setFieldsMappings(
                    updateGorgiasFieldsMappingsLocalized(
                        fieldsMappings,
                        localeCode,
                        {
                            [field]: undefined,
                        }
                    )
                )
            }

            setFieldsMappings(
                updateGorgiasFieldsMappingsLocalized(
                    fieldsMappings,
                    localeCode,
                    {
                        [field]: {
                            source: mapping.source,
                        },
                    }
                )
            )
        }

    return (
        <Container fluid className={settingsCss.pageContainer}>
            <h3>Column matching</h3>

            <p>Match the fields below with the ones in your CSV</p>

            <h4>Language import</h4>

            <p>
                Choose the languages you want to import. If you don’t see all
                languages you wish to import from here, you might need to add
                them in preferences before and re-upload the file.
            </p>

            <ImportedLanguagesTags
                availableLocales={locales}
                selectedLocales={selectedLocalesToImport}
                onSelectedLocalesChange={handleSelectedLocalesChange}
            />

            <TableWrapper className={css['table']}>
                <TableHead>
                    <HeaderCellProperty
                        className={classNames(css['th-auto'])}
                        title="Matched"
                    />
                    <HeaderCellProperty
                        className={css['th-max-20']}
                        title="Gorgias field name"
                    />
                    <HeaderCellProperty
                        className={css['th-max-20']}
                        title="Column header from file"
                    />
                    <HeaderCellProperty title="Preview information" />
                </TableHead>
                <TableBody>
                    {fieldsMappings.map(({localeCode, mappings}) => (
                        <Fragment key={localeCode}>
                            <TableBodyRow>
                                <th
                                    className={
                                        css['table-language-header-cell']
                                    }
                                    colSpan={4}
                                >
                                    <FlagLanguageItem
                                        code={localeCode}
                                        name={
                                            locales.find(
                                                ({code}) => code === localeCode
                                            )?.name || ''
                                        }
                                    />
                                </th>
                            </TableBodyRow>

                            {localeFieldRows.map(
                                ({
                                    field,
                                    fieldName,
                                    isAutoGeneratable,
                                    isRequired,
                                }) => (
                                    <FieldRow
                                        key={field}
                                        fieldName={fieldName}
                                        currentMapping={mappings[field]}
                                        csvColumnsByName={csvColumnsByName}
                                        onChangeMapping={makeOnChangeMapping(
                                            field,
                                            localeCode
                                        )}
                                        isRequired={isRequired}
                                        isAutoGeneratable={isAutoGeneratable}
                                        preview={previewField(
                                            field,
                                            csvColumnsByName,
                                            mappings
                                        )}
                                    />
                                )
                            )}
                        </Fragment>
                    ))}
                </TableBody>
            </TableWrapper>

            <FormGroup className="mt-5">
                <Button
                    className="mr-2"
                    isDisabled={!mappingsComplete(fieldsMappings)}
                    onClick={() => onImport(fieldsMappings)}
                >
                    Confirm Import
                </Button>
                <ConfirmModalAction
                    actions={(onClose) => (
                        <>
                            <Button
                                className={css['cancel-button']}
                                intent="secondary"
                                onClick={() => onClose()}
                            >
                                Cancel
                            </Button>
                            <Button intent="destructive" onClick={onCancel}>
                                Stop Import
                            </Button>
                        </>
                    )}
                    content={
                        <p>
                            You will need to re-upload the file if you decide to
                            import again.
                        </p>
                    }
                    title="Are you sure you want to stop importing?"
                >
                    {(onClick) => (
                        <Button intent="secondary" onClick={onClick}>
                            Cancel
                        </Button>
                    )}
                </ConfirmModalAction>
            </FormGroup>
        </Container>
    )
}

export default CsvColumnMatching
